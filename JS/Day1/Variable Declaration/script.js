//variables and data types

//First rule of declaring variables: we can't use reserved keywords.
//Second rule of declaring variables: we should use meaningful names.
//Third rule of declaring variables: we can't start a variable name with a number.
//Fourth rule of declaring variables: we can't use a space or hyphen in a variable name.
//Fifth rule of declaring variables: we should use camel notation for variable names.

// It's good practice to use var and let keywords to declare variables.

var variablename1 = 'welcome to javascript'; //we can change the value of var variable
//after es6
let variablename2 = 'welcome to javascript again2'; // we can change the value of let variable
const variablename3  = 'welcome to javascript again3'; //we can't change the value of const variable

console.log(variablename1);
console.log(variablename2);
console.log(variablename3);